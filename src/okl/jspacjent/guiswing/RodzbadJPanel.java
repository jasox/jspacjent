  private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
    // ...
    setRodzbad( new Rodzbad() );
    if ( getRodzajeBadan() != null ) {
      getRodzajeBadan().add( getRodzbad() );
    }  
    listModel.addElement( getRodzbad() );    
    updateComponents();
    editedList.setSelectedValue( getRodzbad(), true );        
    if ( ( getRodzbad() != null ) && ( getDao() != null ) ){
      dao.updateRodzbad( getRodzbad() );
    }  
  }//GEN-LAST:event_addButtonActionPerformed

  private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
    // ...
    if ( ( getRodzbad() != null ) && ( getDao() != null ) ) {      
      dao.deleteRodzbad( getRodzbad() );      
    }
    if ( ( getRodzajeBadan() != null ) && ( getRodzbad() != null ) ) {
      getRodzajeBadan().remove( getRodzbad() );
    }  
    if (  getRodzbadSelected() != null ) {
      listModel.removeElement( getRodzbadSelected() );
    }              
    editedList.updateUI();
        
  }//GEN-LAST:event_deleteButtonActionPerformed

