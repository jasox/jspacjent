  /** odœwie¿enie zawartoœci elementów GUI */
  public void updateComponents() {
    if ( getRodzbad() == null ) {      
      opisField.setText( null );
      uwagiField.setText( null );
      opisField.setEnabled( false );
      uwagiField.setEnabled( false );
      deleteButton.setEnabled( false );
      updateButton.setEnabled( false );
    }
    else {
      opisField.setEnabled( true );
      uwagiField.setEnabled( true );
      opisField.setText( getRodzbad().getOpis() );
      uwagiField.setText( getRodzbad().getUwagi() );
      deleteButton.setEnabled( true );
      updateButton.setEnabled( true );
    }  
  }
  
  /** wpisanie edytowanych wartoœci do obiektu */
  public void updateRodzbad() {
    if ( getRodzbad() != null ) {
      getRodzbad().setOpis( opisField.getText().trim() );
      getRodzbad().setUwagi( uwagiField.getText().trim() );
    }  
  }
  
  /** utworzenie modelu listy */
  public void setListModel() {
    listModel.removeAllElements();
    if ( getRodzajeBadan() == null ) {
      Rodzbad a = new Rodzbad();
      setRodzajeBadan( new LinkedList() );
      getRodzajeBadan().add( a );
      listModel.addElement( a ); 
    }
    else {
      Iterator iter = getRodzajeBadan().iterator();
      while ( iter.hasNext() ) {
        listModel.addElement( iter.next() );
      }
    }
  }
  
