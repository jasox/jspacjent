  public void updateComponents() {
    if ( getLekarz() == null ) {
      imieField.setText( null );      
      nazwiskoField.setText( null );      
      uwagiField.setText( null );
      imieField.setEnabled( false );
      nazwiskoField.setEnabled( false );
      uwagiField.setEnabled( false );
      deleteButton.setEnabled( false );
      updateButton.setEnabled( false );
    }
    else {
      imieField.setEnabled(true);
      nazwiskoField.setEnabled( true );
      uwagiField.setEnabled( true );
      imieField.setText( getLekarz().getImie() );      
      nazwiskoField.setText( getLekarz().getNazwisko() );      
      uwagiField.setText( getLekarz().getUwagi() );
      deleteButton.setEnabled( true );
      updateButton.setEnabled( true );
    }  
  }
  
  /** wpisanie edytowanych wartoœci do obiektu */
  public void updateLekarz() {
    if ( getLekarz() != null ) {
      getLekarz().setImie( imieField.getText() );
      getLekarz().setNazwisko( nazwiskoField.getText().trim() );
      getLekarz().setUwagi( uwagiField.getText().trim() );
    }  
  }
  
  /** utworzenie modelu listy */
  public void setListModel() {
    listModel.removeAllElements();
    if ( getLekarze() == null ) {
      Lekarz a = new Lekarz();
      setLekarze( new LinkedList() );
      getLekarze().add( a );
      listModel.addElement( a ); 
    }
    else {
      Iterator iter = getLekarze().iterator();
      while ( iter.hasNext() ) {
        Lekarz a = (Lekarz)iter.next();
        listModel.addElement(a);        
      }      
    }
  }
  
