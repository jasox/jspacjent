  private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
    // ...
    setLekarz( new Lekarz() );
    if ( getLekarze() != null ) {
      getLekarze().add( getLekarz() );
    }  
    listModel.addElement( getLekarz() );    
    updateComponents();
    editedList.setSelectedValue( getLekarz(), true );        
    if ( ( getLekarz() != null ) && ( getDao() != null ) ){
      dao.updateLekarz( getLekarz() );
    }  
  }//GEN-LAST:event_addButtonActionPerformed

  private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
    // ...
    if ( ( getLekarz() != null ) && ( getDao() != null ) ) {      
      dao.deleteLekarz( getLekarz() );      
    }
    if ( ( getLekarze() != null ) && ( getLekarz() != null ) ) {
      getLekarze().remove( getLekarz() );
    }  
    if (  getLekarzSelected() != null ) {
      listModel.removeElement( getLekarzSelected() );
    }              
    editedList.updateUI();
        
  }//GEN-LAST:event_deleteButtonActionPerformed

